    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
            // TODO add your handling code here:
            String idhoadon = PhucVuDAO.getInstance().GetUncheckPhucVuByTableId(idTable);
            String checkId = HoaDonBanHangDAO.getInstance().CheckHoaDonById(idhoadon);
            ThucDon thucdon = (ThucDon) cboDrinks.getSelectedItem();
            ThietBi thietbi = (ThietBi) cboDevice.getSelectedItem();

            if (checkId.equals("-1")) {
                    String id = HoaDonBanHangDAO.getInstance().getIdHoadon();
                    HoaDonBanHangDAO.getInstance().Insert(id, IDkhachhang, "08/11/2020", totalPrice, 0);
                    PhucVuDAO.getInstance().Insert(IDkhachhang, NhanVienDAO1.getInstance().GetAccount().getId(), thietbi.getId(), thucdon.getId(), idTable, id, 0, Integer.parseInt(spnAmount.getValue().toString()) ,Integer.parseInt(spnTime.getValue().toString()));
            } else {
                    PhucVuDAO.getInstance().Insert(IDkhachhang, NhanVienDAO1.getInstance().GetAccount().getId(), thietbi.getId(), thucdon.getId(), idTable, idhoadon, 0, Integer.parseInt(spnAmount.getValue().toString()) ,Integer.parseInt(spnTime.getValue().toString()));
            }
            Connection con = DBUtility.openConnection();
            PreparedStatement pstmt;
            try {
                    pstmt = con.prepareStatement("update `ban` set `trang_thai`=1 where `id_ban`=?");
                    pstmt.setString(1, idTable);
                    pstmt.executeUpdate();                                       
                    displayTables();
                    displayTableDrinks();
                    btnPay.setEnabled(true);
                    cboDrinks.setSelectedIndex(0);
                    cboDevice.setSelectedIndex(0);
            } catch (SQLException ex) {
                    Logger.getLogger(QuanLyThucDon.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnAddActionPerformed

