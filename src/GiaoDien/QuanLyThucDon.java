        private void displayTables() {
                tableModelTables.setRowCount(0);
                List<Ban> listBan = BanDAO.getInstance().LoadListBan();
                for (int i = 0; i < listBan.size(); i++) {
                        Ban ban = listBan.get(i);
                        Object[] dt = {i + 1, ban.getId(), ban.getStatus()};
                        tableModelTables.addRow(dt);
                }
        }

        private void displayTableDrinks() {
                tableModelDrinks.setRowCount(0);
                List<DonDat> listDonDat = ThucDonDAO.getInstance().GetListThucDonByTableId(idTable);
                int totalMenu = 0;
                for (int i = 0; i < listDonDat.size(); i++) {
                        DonDat dondat = listDonDat.get(i);
                        Object[] dt = {i + 1, dondat.getTenmon(), dondat.getGia(), dondat.getSoluong(),dondat.getTenthietbi(), dondat.getThoigian(), dondat.getTongtien()};
                        totalMenu += dondat.getTongtien();
                        tableModelDrinks.addRow(dt);
                }
                totalPrice = totalMenu;
                txtTotalPrice.setText(totalPrice + "");
        }

        private void loadThucDon() {
                List<ThucDon> listThucDon = ThucDonDAO.getInstance().GetListThucDon();
                for (ThucDon thucdon : listThucDon) {
                        comboBoxModel.addElement(thucdon);
                }
        }
        private void loadThietBi() {
                List<ThietBi> listThietBi = ThietBiDAO.getInstance().getListThietBi();
                for (ThietBi thietbi : listThietBi) {
                        cboDevice.addItem(thietbi);
                }
        }
        
        private void loadIDKhachHang() {
            txtKhachHang.setText(getIDkhachhang());
        }
